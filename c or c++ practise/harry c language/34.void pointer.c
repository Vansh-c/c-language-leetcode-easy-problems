// void pointer is a pointer which has no datatype associated with in c 
// void pointer can be easily typecasted into another pointer type . 

// In simple language it is a general purpose pointer variable . 

//  ex: int x = 10 ;
//      char y = 'x'.

// void *p = &x;     // void pointer stores address of integer 'x'.
// p = &y      // void pointer now holds address of character y . 


// in dynamic memory allocation , malloc() and calloc() return (void*) type pointer . 

// This allowes these dynamic memory function to be used to allocate memory of any data type . 
// this is because these pointers can be typecasted into any data type .


// It is not possible to dereference void pointers .,
//pointer arithmetic is not allo0wed in c standards with void pointers .
//hence , it ins not recommended to use pointer arithmetic with void pointers .


#include <stdio.h>
#include <string.h>


int main()
{
    int a = 345;
    float b = 8.3;
    void *ptr ;
    ptr = &a;
    printf("the value of a is %d \n",*(int*)ptr);        // we converted void pointer into integer pointer
    ptr = &b;
    printf("the value of b is %f \n",*(float*)ptr);

    return 0 ;
}








































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































